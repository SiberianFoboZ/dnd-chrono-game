        <h2>Валлаки и демон</h2>
        <p>
          Всего три шага от лагеря вистани — и судьба, как всегда, подкидывает нам «новые проблемы». Будто я не привыкла к тому, что спокойствие — это роскошь, которой мне не видать. Место, по местным меркам, даже можно назвать живописным: водопад, лес, крепкий мост… Ха! И, разумеется, горгульи. Куда же без этих каменных уродцев в наших «великих приключениях»?
Стоило нам ступить на этот злосчастный мост, как эти пренеприятнейшие статуи ожили и кинулись на нас, будто всерьёз полагая, что смогут меня впечатлить. Как мило. Как трогательно. И как предсказуемо. Мы, разумеется, расправились с ними с лёгкостью, достойной баллад — хотя, признаться, даже петь об этом не хочется: слишком уж скучным вышел бой.
Пока я, как истинный знаток ценностей и единственная в нашей компании особа с тонким вкусом, извлекала прекрасные красные драгоценные камушки из обломков этих нелепых тварей, наша драгоценная подопечная Ирина, видимо, решила, что настал её звёздный час… и попыталась самоубиться.
Боже… Ну даже если ты заметила, как что‑то блестит под водопадом, насколько же мало мозгов нужно иметь, чтобы сломя голову кинуться в ледяную воду и пытаться достать это самостоятельно? Неужели не ясно, что для таких подвигов у нас есть Артур? Достаточно было лишь бросить на него один томный взгляд — и он бы весь мир бросил к твоим миниатюрным ножкам. Впрочем, просить его не пришлось: стоило Ирине поскользнуться и упасть, как он уже мчался к ней, словно его кто‑то заклинанием подтолкнул. Герой, что тут скажешь.
А потом на них ещё и водяной мефет напал. Как будто одного безумия было мало. В итоге все найденные «сокровища» достались Ирине — хотя «сокровища» — это чересчур громкое слово для горстки монет, колечка и рапиры. Правда, рапира оказалась магической и даже потом поговорила с Ириной во сне. Ну хоть что‑то.
Единственный плюс во всей этой суете — мы наконец смогли выдохнуть и даже немного помыться, нагрев воду над костром. И почему только я одна замечаю красоту человеческих тел? Ирина с Исмарком, понятное дело, ещё не отошли от трагедии: погружены в свои страдания, смотрят в одну точку и не видят ничего вокруг. Но почему Артур и Малбрин так безразличны к нашим новым спутникам? Словно мир для них — это просто набор препятствий, а не полотно, полное оттенков, линий и изящества.
Что ж, если они не хотят замечать красоту этого мира добровольно, я насильно её им продемонстрирую. У меня свои методы, и они, поверьте, безотказны.
Малбрин, надо признать, всё ещё прячется за собственной тенью — слишком скрытная, будто боится, что малейшее слово выдаст в ней какую‑то великую тайну. Посягать на её вещи я не стала. Слишком утомительно. А вот раздобыть, полистать и подправить дневник Артура оказалось до смешного просто. Наш мальчик пишет! Да, поначалу слог у него хромает на обе ноги так, что из глаз текут кровавые слезы, но чем дальше погружаешься в эти каракули, тем отчётливее видно: прогресс есть. Меня прямо гордость берёт за его успехи — пусть и скромные. Возможно, лет через пять я даже смогу назвать его записи сносными. По очень снисходительным меркам, разумеется.
По доброте душевной (а у меня её, представьте, хватает — просто она достаётся лишь избранным) я подправила все неточности, выправила корявые обороты и даже оставила небольшой «подарочек» на последних страницах. «Влажная Ирина для влажных снов» — и ведь даже стилистику его рисунков сохранила: всё такое небрежно‑простое, одноцветное, уныло‑скучное. Искусство должно быть выше этого, но ради шутки можно и опуститься до его уровня.
Малбрин, кстати, заметила, как я колдовала над дневником. Я чуть не рассмеялась в голос, когда с очень серьёзным видом заявила ей: «Ну это же личная вещь, её нельзя читать». Ха‑ха! Ещё как можно, моя милая, — просто сначала нужно привести всё в порядок. Кто, если не я, научит этих простаков, как достойно фиксировать свои приключения?
По пути нам попался местный пьяница — тот ещё кладезь сплетен. Нес какую‑то чушь про тропы, про засады, про «тёмные дела» в лесах… Я слушала вполуха: подобные страшилки годятся разве что для того, чтобы пугать детей у костра. Но пару имён и названий всё же запомнила — мало ли, вдруг пригодятся, чтобы кого‑нибудь эффектно удивить своей осведомлённостью.
А потом случилось настоящее событие: мы встретили Петра! Странствующий торговец, которого в юности помогал растить мой дедушка. Он сразу меня признал. Он даже провёл со мной обряд крови — теперь мы побратимы. Звучит куда благороднее, чем «просто знакомые», не находите? Петра подвёз нас до Валлаки — или как там называют это богом забытое место. По дороге он рассказывал мне значения слов на языке вистани, и я ловила каждое: пора учить родной язык. Пожалуй, даже сделаю небольшой словарик на последних страницах дневника. 
Кстати, именно в дороге Артур и обнаружил мой «подарочек» в дневнике. Ох, стоило видеть его лицо! Сначала он завелся, принялся читать очередную нудную лекцию о неприкосновенности личных записей — будто я не понимаю элементарных вещей. Потом дошёл до той самой страницы, замер, осмотрелся по сторонам, будто искал, кто ещё мог это увидеть, снова посмотрел на рисунок, покраснел до корней волос и захлопнул дневник так резко, что, кажется, чуть не оторвал обложку. Я хихикала всю дорогу, а Малбрин с Ириной безуспешно пытались забрать у него этот дневник и посмотреть на что же он так отреагировал.
Валлаки оказался ещё более негостеприимным, чем я предполагала. Нас даже в город пускать не хотели, пока Артур — вы только вдумайтесь, наш праведный святоша! — не сунул стражникам пару монет. И что же? Пустили ли нас после этого? Черта с два! Тут, видите ли, ещё и правила есть: оружие сдай, веди себя прилично ещё и бургомистра с его фестивалями и праздниками не критикуй.
Нет, мне, конечно, и самой по душе ярмарки, выступления, вся эта праздничная суета. Я бы с радостью приняла участие в местных торжествах — да только сажать в темницу всякого, кто посмеет высказать хоть тень недовольства, — это уже перебор. Искусство, радость и счастье должны быть свободными, а не навязанными под угрозой решётки. 
Валлаки и так не радовал глаз ни архитектурой, ни атмосферой, но стоило нам зайти в таверну — и моё настроение рухнуло ниже подвального погреба, где держат самое кислое вино. Откуда, чёрт возьми, в этом захолустье взяться второму барду? Я вхожу — а никто даже головы не повернул! Все пялились на его выступление, будто этот жалкий любитель способен выдать хоть одну ноту, достойную упоминания в приличной балладе.
К счастью, Риктавио оказался ещё большим профаном, чем я предполагала. Достаточно было бросить пару едких замечаний о его ритме и совершенно безнадёжной интонации — и бедняга уже смотрел на меня как на богиню, спустившуюся с небес, чтобы спасти его от позора. Без особого труда уговорила его стать моим подмастерьем. Теперь на сцене будет только одна звезда — я. И пусть попробует возразить.
Исмарк с Ириной опять устроили эту свою бесконечную драму — снова поругались, и наша вспыльчивая подопечная, гордо взметнув плащом, покинула таверну, словно героиня дешёвой трагедии. Само собой, я отправила Артура на её поиски. Надеюсь, мой «подарочек» в дневнике хоть немного подтолкнёт этого тугодума к действиям — пусть наконец поймёт, что героизм — это не только размахивать мечом, но и вовремя поймать ту, которая вот-вот сорвётся с обрыва.
Малбрин, по моим ощущениям, успела поболтать с половиной бара, собирая слухи, будто это не разговоры, а драгоценные камни, которые надо успеть схватить, пока не растащили. А ещё она помогла мне облапошить двух подвыпивших грубиянов — и вышло это до того изящно, что я почти гордилась нами обеими. Мне не составило труда отвлечь их разговором, рассыпая комплименты так щедро, будто это конфетти на празднике. В тот самый момент, когда их внимание было целиком приковано ко мне, моя подруга незаметно подкралась сзади и ловко подрезала у одного из них кошель. Награда, разумеется, поделена поровну.
Мне нравятся такие наши девчачьи вылазки — пусть и непродолжительные, зато весёлые, полезные и прибыльные. Делать что-то дерзкое без душных лекций о морали — как глоток свежего воздуха после затхлой комнаты.
Исмарк, к слову, напился в хлам — так, что даже стоять мог только прислонившись к стене. Пришлось провожать его до комнаты. Решила поспать рядом: пусть это будет не романтикой, а стратегией — дам шанс ребятам сблизиться, раз уж судьба (ну или я) так настойчиво подталкивает их друг к другу. По дороге он бормотал пьяный бред, и мне, конечно, льстило, что он всё же обратил на меня внимание — ну а как иначе, я же прекрасна. Но всерьёз полагать, что я могла бы остаться подле него в роли верной покорной жены, рожать детей, забыть о сцене и подчиняться чьим-то приказам? Ха‑ха! Милый, я тут главная скрипка, и всё будет так, как я того хочу.
Всё, что произошло после, я и вспоминать не хочу, не то что записывать. Но и забыть об этом кошмаре у меня вряд ли когда‑то получится. Дневник — единственное место, куда можно выплеснуть эмоции, не рискуя показаться слабой перед остальными. Да и если я всё же не выживу — а сейчас это кажется до обидного вероятным, — возможно, кому‑то мои записи спасут жизнь.
Надо же, спала рядом с Исмарком. И ведь была минута, когда я могла поступить… скажем так, менее благородно и воспользоваться его хмельным состоянием. Возможно, тогда вся эта нелепая трагедия не разыгралась бы столь унизительно. Но нет — я предпочла сохранить достоинство. И вот награда: нас застали врасплох, словно кучку неопытных новичков.
Пробуждение вышло… впечатляющим. Я очнулась среди ночи в холодном поту, с кристально ясной мыслью: что-то не так. И это, позвольте заметить, заслуга не слепого везения, а моего исключительного слуха — того самого, что различает шёпот за три комнаты и улавливает малейшую перемену в дыхании. Именно благодаря ему я расслышала какой-то посторонний, чуждый оттенок в тишине… И, смею надеяться, этим спасла не только себя, но и всю эту беспечную компанию.
Над Исмарком висел демон — маленький, рогатый, будто сошедший со страниц дешёвого бестиария для начинающих приключенцев. Словно кто‑то решил устроить мне персональное испытание на прочность, да ещё и с дурным вкусом в декорациях. От твари к Исмарку тянулась эта мерзкая нить чёрной дымки, как будто из него высасывали саму душу.
Я не раздумывала. Арбалет лежал там, где ему и положено быть — под рукой, потому что в этом мире рассчитывать можно только на себя и на оружие, которое ты не выпускаешь из поля зрения. Выстрел ушёл точно. Но эта тварь… Она ответила воплем, который ударил не по ушам — по самой сути сознания. Меня швырнуло в стену так, что на мгновение реальность рассыпалась на осколки.
Я орала. Не «звала на помощь», как пишут в дешёвых хрониках, — я кричала, вкладывая в голос всю силу, на которую была способна. Артур, Малбрин, Ирина, Риктавио, хоть кто‑нибудь, кто умеет думать быстрее, чем улитка на прогулке! Но комната, казалось, пожирала звуки, превращая их в жалкое эхо. Я металась по углам, пыталась укрыться от атак, до хрипоты звала на помощь, снова атаковала… и в какой‑то момент поняла: если сейчас не сделаю что‑то радикальное, эта ночь станет финальным актом моей истории. Поэтому я выпила зелье лечения. То самое, редкое, безумно дорогое, я берегла для действительно достойного случая. А пришлось потратить его, чтобы просто не потерять сознание после очередного удара об стену. Досадно до зубного скрежета.
Всегда думала, что смерть в бою — это что-то величественное: ты стоишь в луже собственной крови, но с гордо поднятой головой, произносишь последнюю, идеально выстроенную фразу, и мир замирает, чтобы запомнить твой уход. А не вот это: в одной ночной рубашке, растрёпанная, без капли макияжа, на грязном полу какой-то замшелой забегаловки, в окружении затхлого воздуха. Ощущения, кстати, отвратительные: холод, слабость, боль, будто каждая капля силы вытекает сквозь пальцы, а сознание ускользает, как недовольный зритель с неудачного представления.
Последнее, что помню, — прохладную ладонь Артура на своём лбу. И этот внезапный прилив сил, словно кто‑то вернул свет на сцену в самый тёмный момент. Спасибо, здоровяк. Ты, может, и не умеешь подбирать слова, но в нужный момент оказываешься ровно там, где нужен. Я в долгу не останусь — не из чувства обязанности, а потому, что я не из тех, кто забывает настоящую поддержку.
А потом стало по-настоящему жутко. Демон завладел Исмарком, и это было зрелище, от которого даже у меня, привыкшей к самым мрачным театральным постановкам, кровь стыла в жилах. Его глаза горели красным, он был сам на себя не похож — словно кто-то стёр личность и заменил её пугающей пародией. Мы кое‑как скрутили его, связали и доставили в ближайшую церковь для обряда экзорцизма. Который, к слову, провалился с таким грохотом, что даже у церковников хватило такта выглядеть смущёнными.
И вот тут судьба, видимо, решила, что и так уже достаточно нас помучила: демон по какой-то неведомой мне причине сам покинул тело Исмарка и растворился в ночи, будто понял, что оказался не на той сцене и публика ему не по зубам. Что ж, пусть уходит. У нас и без него хватает дел.
Я долго не могла прийти в себя. Впервые смерть оказалась не величественной строфой в героической балладе, а грубой, уродливой реальностью — без пафоса, без фанфар, просто тьма и удар о стену, после которого мир на мгновение перестаёт звучать. А тишина эта страшнее любого вопля.
Мне так хотелось верить в простую, уютную ложь: что в городе, в таверне, под крышей, в постели я в безопасности, а все монстры где‑то там, в лесу, далеко, и не достанут до нас. Как же я ошибалась… И хуже всего не сам страх, а стыд от собственной наивности. Я, которая годами училась различать фальшь в каждом слове, не заметила, как сама поверила в эту дешёвую иллюзию покоя.
Сам факт того, что тварь пробралась к нам в комнату и напала во сне, пугает до дрожи — словно кто‑то доказал, что все мои уловки, все навыки, вся моя хваленая интуиция ничего не стоят перед слепой случайностью. А стоит только представить, что я могла проснуться в одной постели с трупом… или не проснуться вовсе, — и по коже бегут ледяные мурашки, будто кто‑то провёл по спине когтями.
Где вообще безопасное место в этом городе? Где можно спокойно спать, не прислушиваясь к каждому шороху? Где отдохнуть и не вздрагивать от скрипа половицы? Кому можно доверять, если даже церковь оказалась не святилищем, а очередным звеном в цепи чьей‑то глупости? Этот священник… Как можно было проболтаться о месте хранения священной реликвии какому‑то мальчишке? А тот, разумеется, разнёс новость по всему городу, как сплетню о чужой неверности, не понимая, что играет с огнём.
С дальнейшими событиями нам ещё только предстоит разобраться, но итог у всего этого один: защита пала, и у нас всех будут огромные проблемы, если мы не найдём эти злосчастные кости всего за два дня. Два дня… Смехотворно мало, когда на кону не просто репутация, а сама возможность проснуться завтра. И теперь мне придётся не просто действовать, а действовать безупречно — потому что любая ошибка будет стоить слишком дорого.
Если в ближайшее время ещё выдастся свободная минутка — хотя бы одна, по-настоящему безопасная, — мне будет о чём подумать и что переосмыслить. Как минимум столь легкомысленно к нашему приключению я относиться больше не буду. Настало время взрослеть, становиться сильнее, серьёзнее и решать проблемы, а не прятаться за изящными словами и красивыми позами. В конце концов, я ради этого сюда и приехала.
Это же только в сказках и балладах мир спасают наивные весельчаки с горящими глазами и верой в чудо. В реальности таких обводят вокруг пальца первыми и бросают помирать в канаве, даже не вспомнив их имён. Да, звучит непоэтично. Зато правдиво — а иногда правда важнее рифмы.
