        донимала и меня, но я не понял, что ей от меня было нужно. Вечно
        называет меня красавчиком и просит помочь ей с переносом вещей, словно
        носильщика. Что касается Дроу, ходит вечно недовольной, хотя на мое
        удивление, хоть она и умеет вскрывать замки и часто, с прищуром в
        глазах, в солнечный день. Мой кошелек никогда не пропадал.
      </p>
      <p>
        Первый раз, я решил с ними на простое задание…
        <span class="underline-wavy">я так думал.</span>
      </p>
