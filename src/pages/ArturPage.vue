        земли.
      </div>
      <p>
        Одно задание запомнилось особенно. Неопытный волшебник решил
        попрактиковаться в магии, случайно поднял мертвеца, не смог его
        контролировать и запер в комнате. Никогда не забуду, как долго читал ему
        лекцию о некромантии и о том, почему это плохо.
